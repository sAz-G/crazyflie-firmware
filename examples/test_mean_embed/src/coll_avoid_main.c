static uint8_t  state = 0;
static float    thrusts[4];
static uint16_t thrustsToMotor[4];
static uint8_t  isHighLevelController = 0;
static float    mototrKValue = 1.0f;
static int timer = 0;

void getThrusts(uint16_t *thrst)
{
  thrst[0] = thrustsToMotor[0];
  thrst[1] = thrustsToMotor[1];
  thrst[2] = thrustsToMotor[2];
  thrst[3] = thrustsToMotor[3];
}

uint8_t isHighLevel()
{
  return isHighLevelController;
}

void appMain()
{
  static Vector3 lastPosition;
  vTaskDelay(M2T(1000));
  static int t_counter = 0;
  while (1)
  {
    vTaskDelay(M2T(10));

    lastPosition = getPosition();

    switch (state)
    {
    case 0: // do nothing
      break;
    case 1: // start
      if(t_counter >= 900)
      {
        state = 2;
        continue;
      }
      for (int k = 0; k < 4; k++)
      {
        thrustsToMotor[k] = (uint16_t)(UINT16_MAX * testThrustFloat[t_counter][k] * (mototrKValue));
        thrusts[k] = testThrustFloat[t_counter][k];
        t_counter++;
        //t_counter = (t_counter >= 900) ? 0 : t_counter;
      }
      break;
    case 2: // land

        if (getPosition().z < 0.15f)
        {
          for (int k = 0; k < 4; k++)
          {
            thrustsToMotor[k] = (uint16_t)(UINT16_MAX * (0.0f));
          }
          state = 0;
        }

      break;
    default:
      break;
    }

    thrusts[timer]   = 0.5f*testThrustFloat[timer][0];
    thrusts[timer]   = 0.5f*testThrustFloat[timer][1];
    thrusts[timer]   = 0.5f*testThrustFloat[timer][2];
    thrusts[timer]   = 0.5f*testThrustFloat[timer][3];
    timer++;
    timer = timer >= 900 ? 1 : timer;

  }
}

